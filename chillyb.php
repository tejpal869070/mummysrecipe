                            <b>Red Chilli Pickle Recipe</b>
                            <p>Ingredients:</p>

                            <ul>
                                <li>   Red Chili - 8pcs</li>
                                <li>   Oil - 3 tbsp</li>
                                <li> Black Pepper - 10 pcs</li>
                                <li>   Asafoetida - 1 tbsp</li>
                                <li>  Kasuri Methi - 150 gm </li>
                                <li>   Mustard Seeds - 2 tbsp</li>
                                
                                <li>   Fenugreek Seeds - 1 tbsp</li>
                                 <li>   Dry Dates - 4-5 pcs</li>
                                  <li>  Red Chili Powder - 1 tbsp</li>
                                   <li>   Kashmiri Red Chili Powder - 1 tsp</li> 
                                   <li>   Lemon Juice - 1 tbsp</li>
                                   <li>   Salt - 1 tsp</li>
                                   <li>   Jaggery - 200 gm</li>
                            </ul>
                            <p>It is very easy to make pickle. It is not complicated or messy as you think it is. You have to start with carefully tempering all the spices and herbs together in a way that it does not burn. Therefore, keep your flame medium-high. Once the tempering is ready, add red chilli and jaggery to it. Make sure the jaggery is cut into fine pieces so that it melts fast. Within some time, the herbs and spices will start releasing their flavors, making the pickle taste even better.</p>
                        <p>Once the mixture cools down, your chilli pickle is ready! Now store it in a glass container or a porcelain pot and enjoy it as an accompaniment with any dish you like.</p>
                        
